//========================================================================
// 函数: Display_init
// 描述: 用户初始化程序.
// 参数: None.
// 返回: None.
// 版本: V1.0, 2022-05-26
//========================================================================
void Display_init(void)
{
    for(display_index=0; display_index<8; display_index++)
    {
        LED8[display_index] = DIS_BLACK; //初始化上电显示内容
    }
    display_index = 0;
}

//========================================================================
// 函数: Sample_Display
// 描述: 用户应用程序.
// 参数: None.
// 返回: None.
// 版本: V1.0, 2022-05-26
//========================================================================
void Sample_Display(void)
{
    P7 = ~T_COM[7-display_index];
    P6 = ~t_display[LED8[display_index]];
    if(++display_index >= 8)    display_index = 0;  //8位结束回0
}

